SIGNS = (
    ("Водолей", 20),
    ("Риби", 19),
    ("Овен", 21),
    ("Телец", 21),
    ("Близнаци", 21),
    ("Рак", 21),
    ("Лъв", 22),
    ("Дева", 23),
    ("Везни", 23),
    ("Скорпион", 23),
    ("Стрелец", 22),
    ("Козирог", 22)
)


def what_is_my_sign(day, month):
    if day >= SIGNS[month - 1][1]:
        return SIGNS[month - 1][0]
    else:
        return SIGNS[month - 2][0]
